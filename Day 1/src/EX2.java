import java.util.Scanner;

public class EX2 {

	public static void main(String[] args) {
		short a,b;
		Scanner sc=new Scanner(System.in);
		System.out.println("enter a value:");
		a=sc.nextShort();
		System.out.println("enter b value:");
		b=sc.nextShort();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

	}

}
